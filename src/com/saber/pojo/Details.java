public class Details {

    private String details_type;//课程内容
    private String details_class;//课程课时
    private String details_name;//课时名称
    private String details_link;//课程链接

    public String getDetails_type() {
        return details_type;
    }

    public void setDetails_type(String details_type) {
        this.details_type = details_type;
    }

    public String getDetails_class() {
        return details_class;
    }

    public void setDetails_class(String details_class) {
        this.details_class = details_class;
    }

    public String getDetails_name() {
        return details_name;
    }

    public void setDetails_name(String details_name) {
        this.details_name = details_name;
    }

    public String getDetails_link() {
        return details_link;
    }

    public void setDetails_link(String details_link) {
        this.details_link = details_link;
    }

}
